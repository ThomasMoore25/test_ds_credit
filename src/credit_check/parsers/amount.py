"""Парсер денежных сумм.

Поддерживаемые форматы (покрывают датасет):
    1 250 000,00 руб.       — рус. формат, разделитель тысяч — пробел, дробная часть — запятая
    1250000.00 ₽            — без разделителя тысяч, точка как дроб. разделитель
    1,250,000.00 RUB        — англ. формат, запятая — разделитель тысяч, точка — дробная
    500000.00 ₽             — то же без разделителя тысяч
    «девятьсот тысяч рублей» — сумма прописью (fallback)

Все числовые форматы требуют контекста (руб / ₽ / RUB / сумма / итого / стоимость / оплат),
иначе ИНН, БИК, р/с и пр. длинные числа ошибочно детектируются как сумма.
"""

from __future__ import annotations

import re
from typing import Final

# --- Контекст -----------------------------------------------------------------

# Контекстные слова, которые часто сопровождают сумму в документах.
# Узкий список: только явные маркеры суммы. Слово «оплат» ИСКЛЮЧЕНО — оно часто
# соседствует с датами («Оплата до: 28/02/25»), что даёт ложные срабатывания на р/с.
# «Договор» тоже исключено — встречается в шапке рядом с ИНН.

# ЛЕВЫЙ контекст — слова, идущие ДО суммы (обычно «Итого:», «Стоимость:», «Сумма:»)
_LEFT_CONTEXT_WORDS: Final[tuple[str, ...]] = (
    "стоимость",
    "сумма",
    "сумму",
    "итого",
    "к оплате",
)

# ПРАВЫЙ контекст — слова, идущие ПОСЛЕ суммы («руб.», «₽», «RUB»).
# Окно шире (60 символов), потому что между числом и словом «рублей» часто
# стоит сумма прописью: «1 250 000,00 (Один миллион двести пятьдесят тысяч) рублей».
_RIGHT_CONTEXT_WORDS: Final[tuple[str, ...]] = (
    "руб",
    "₽",
    "rub",
)

# --- Шаблоны числовых сумм ----------------------------------------------------
# Каждый шаблон компилируется БЕЗ захвата валюты — контекст проверяется отдельно
# через окно вокруг совпадения. Это позволяет корректно отличать сумму от ИНН.

_NUMERIC_PATTERNS: Final[list[re.Pattern[str]]] = [
    # 1 250 000,00 (рус. формат с разделителем тысяч — пробелом)
    re.compile(
        r"(?<![\d.,])"
        r"\d{1,3}(?:[ \u00A0]\d{3})+"
        r"(?:,\d{1,2})?"
    ),
    # 1,250,000.00 (англ. формат с запятой как разделителем тысяч)
    re.compile(
        r"(?<![\d.,])"
        r"\d{1,3}(?:,\d{3})+"
        r"(?:\.\d{1,2})?"
    ),
    # 1250000.00 / 500000.00 — 4+ цифр, опционально с дробной частью через точку
    re.compile(
        r"(?<![\d.,])"
        r"\d{4,}(?:\.\d{1,2})?"
    ),
    # Короткие суммы (1-3 цифры) с обязательной валютой сразу после:
    # «100 руб.», «50 ₽», «200 RUB», «99,50 руб.»
    # Требуется валюта, чтобы не матчить «№ 12», «01.03.2025» и пр.
    re.compile(
        r"(?<![\d.,])"
        r"\d{1,3}(?:[.,]\d{1,2})?"
        r"\s*(?:руб(?:лей|ля|ль|\.|\b)|₽|RUB)",
        re.IGNORECASE,
    ),
]

# Сокращения: «тыс. руб.», «млн руб.», «млрд руб.»
# Преобразуем в полные числа: 1250 тыс. руб. → 1 250 000 руб.
_ABBREVIATION_RE: Final[re.Pattern[str]] = re.compile(
    r"(\d+(?:[.,]\d+)?)\s*(тыс\.?|млн\.?|млрд\.?)\s*руб",
    re.IGNORECASE,
)

_ABBREVIATION_MULTIPLIER: Final[dict[str, float]] = {
    "тыс.": 1_000, "тыс": 1_000,
    "млн.": 1_000_000, "млн": 1_000_000,
    "млрд.": 1_000_000_000, "млрд": 1_000_000_000,
}


def _expand_abbreviation(text: str) -> str | None:
    """Заменяет «1250 тыс. руб.» на «1 250 000 руб.» в тексте.
    Возвращает модифицированный текст или None, если сокращений нет.
    """
    def replace(m: re.Match[str]) -> str:
        num = float(m.group(1).replace(",", "."))
        mult = _ABBREVIATION_MULTIPLIER[m.group(2).lower()]
        result = num * mult
        if result == int(result):
            return f"{int(result)} руб"
        return f"{result} руб"

    new_text, count = _ABBREVIATION_RE.subn(replace, text)
    return new_text if count > 0 else None


# Шаблон суммы прописью — охватывает базовые числительные до триллионов.
# Между числительными и словом «рублей» допускается закрывающая скобка (для
# формата «1 250 000,00 (Один миллион двести пятьдесят тысяч) рублей»).
# Включает дробные числительные «полтора», «полтораста», «полсотни» и т.п.
_SPELL_OUT_RE: Final[re.Pattern[str]] = re.compile(
    r"("
    r"(?:один|одна|две|два|три|четыре|пять|шесть|семь|восемь|девять|десять|"
    r"одиннадцать|двенадцать|тринадцать|четырнадцать|пятнадцать|шестнадцать|"
    r"семнадцать|восемнадцать|девятнадцать|двадцать|тридцать|сорок|пятьдесят|"
    r"шестьдесят|семьдесят|восемьдесят|девяносто|сто|двести|триста|четыреста|"
    r"пятьсот|шестьсот|семьсот|восемьсот|девятьсот|"
    r"тысяч|тысяча|тысячи|миллион|миллиона|миллионов|"
    r"миллиард|миллиарда|миллиардов|"
    r"триллион|триллиона|триллионов|"
    r"полтора|полтораста|полсотни|полтысячи|полмиллиона|полмиллиарда|"
    r"ноль|нуль"
    r")(?:\s+(?:"
    r"один|одна|две|два|три|четыре|пять|шесть|семь|восемь|девять|десять|"
    r"одиннадцать|двенадцать|тринадцать|четырнадцать|пятнадцать|шестнадцать|"
    r"семнадцать|восемнадцать|девятнадцать|двадцать|тридцать|сорок|пятьдесят|"
    r"шестьдесят|семьдесят|восемьдесят|девяносто|сто|двести|триста|четыреста|"
    r"пятьсот|шестьсот|семьсот|восемьсот|девятьсот|"
    r"тысяч|тысяча|тысячи|миллион|миллиона|миллионов|"
    r"миллиард|миллиарда|миллиардов|"
    r"триллион|триллиона|триллионов|"
    r"полтора|полтораста|полсотни|полтысячи|полмиллиона|полмиллиарда"
    r"))*"
    r"\)?\s+(?:рублей|рубля|рубль)"
    r")",
    re.IGNORECASE,
)

# --- Словарь числительных для прописи -----------------------------------------

_UNITS: Final[dict[str, int]] = {
    "ноль": 0, "нуль": 0,
    "один": 1, "одна": 1,
    "два": 2, "две": 2,
    "три": 3, "четыре": 4, "пять": 5, "шесть": 6, "семь": 7,
    "восемь": 8, "девять": 9, "десять": 10, "одиннадцать": 11,
    "двенадцать": 12, "тринадцать": 13, "четырнадцать": 14,
    "пятнадцать": 15, "шестнадцать": 16, "семнадцать": 17,
    "восемнадцать": 18, "девятнадцать": 19, "двадцать": 20,
    "тридцать": 30, "сорок": 40, "пятьдесят": 50, "шестьдесят": 60,
    "семьдесят": 70, "восемьдесят": 80, "девяносто": 90,
}

_HUNDREDS: Final[dict[str, int]] = {
    "сто": 100, "двести": 200, "триста": 300, "четыреста": 400,
    "пятьсот": 500, "шестьсот": 600, "семьсот": 700,
    "восемьсот": 800, "девятьсот": 900,
}

# Масштабы: тысячи, миллионы, миллиарды, триллионы
_SCALE: Final[dict[str, int]] = {
    "тысяча": 1_000, "тысячи": 1_000, "тысяч": 1_000,
    "миллион": 1_000_000, "миллиона": 1_000_000, "миллионов": 1_000_000,
    "миллиард": 1_000_000_000, "миллиарда": 1_000_000_000, "миллиардов": 1_000_000_000,
    "триллион": 1_000_000_000_000, "триллиона": 1_000_000_000_000,
    "триллионов": 1_000_000_000_000,
}

# Дробные числительные: «полтора» (1.5), «полтораста» (150) и др.
_FRACTIONAL: Final[dict[str, float]] = {
    "полтора": 1.5,
    "полтораста": 150.0,
    "полсотни": 50.0,
    "полтысячи": 500.0,
    "полмиллиона": 500_000.0,
    "полмиллиарда": 500_000_000.0,
}


def _spellout_to_int(text: str) -> int | None:
    """Преобразует строку числительных прописью в int.

    Поддерживает форматы:
        «один миллион двести пятьдесят тысяч»,
        «девятьсот тысяч»,
        «пятьсот тысяч»,
        «полтора миллиона»,
        «два миллиарда триста миллиона».

    Возвращает None, если разобрать не удалось.
    """
    tokens = [t for t in re.split(r"\s+", text.strip().lower()) if t]
    if not tokens:
        return None

    total = 0.0
    current = 0.0
    saw_number = False

    for tok in tokens:
        if tok in _UNITS:
            current += _UNITS[tok]
            saw_number = True
        elif tok in _HUNDREDS:
            current += _HUNDREDS[tok]
            saw_number = True
        elif tok in _SCALE:
            if not saw_number:
                # «тысяч» без предшествующего числа — подразумеваем 1
                current = 1
            total += current * _SCALE[tok]
            current = 0
            saw_number = True  # scale тоже считаем числом
        elif tok in _FRACTIONAL:
            current += _FRACTIONAL[tok]
            saw_number = True
        elif tok in {"рублей", "рубля", "рубль", "руб"}:
            continue
        else:
            return None

    total += current
    # Если число дробное (например, «полтора миллиона» = 1.5 * 10^6 = 1_500_000),
    # оно может быть float; приводим к int, если оно целое
    if total == int(total):
        return int(total) if saw_number else None
    return int(total) if saw_number else None


def _numeric_match_to_float(match: str) -> float | None:
    """Преобразует распознанный числовой фрагмент в float.

    Эвристики:
      * если есть и `,` и `.` → англ. формат (запятая — тысячи, точка — дробная)
      * если есть только `,` в позиции дробной (последняя) → рус. дробная
      * если есть только `,` в позиции тысяч (несколько запятых) → англ. thousands
      * пробел/неразрывный пробел — всегда разделитель тысяч
    """
    s = match.strip()
    if not s:
        return None

    # Отрезаем валюту («руб.», «₽», «RUB»), если она есть в raw
    # (новый паттерн для коротких сумм включает валюту в match)
    s = re.sub(r"\s*(?:руб(?:лей|ля|ль|\.|\b)?|₽|RUB)\s*$", "", s, flags=re.IGNORECASE).strip()
    if not s:
        return None

    has_comma = "," in s
    has_dot = "." in s

    if has_comma and has_dot:
        # Английский формат: 1,250,000.00
        s = s.replace(",", "")
    elif has_comma:
        parts = s.split(",")
        if len(parts) == 2 and len(parts[1]) <= 2:
            # Дробная часть: 1 250 000,00 → 1250000.00
            s = parts[0].replace(" ", "").replace("\u00A0", "") + "." + parts[1]
        else:
            # Запятые — разделители тысяч
            s = s.replace(",", "").replace(" ", "").replace("\u00A0", "")
    elif has_dot:
        # Точка — либо дробная, либо разделитель тысяч.
        # В датасете точка в позиции 3-й с конца — дробная.
        parts = s.split(".")
        if len(parts) == 2 and len(parts[1]) <= 2:
            pass  # уже корректный формат
        else:
            s = s.replace(".", "")
    else:
        s = s.replace(" ", "").replace("\u00A0", "")

    s = s.strip()
    if not s or not re.fullmatch(r"\d+(?:\.\d+)?", s):
        return None

    return float(s)


def _has_context(text: str, start: int, end: int) -> bool:
    """True, если в окрестности совпадения есть контекстное слово суммы.

    ЛЕВЫЙ контекст (маркеры «Итого:», «Стоимость:», «Сумма:») — узкое окно 25 символов,
    т.к. эти слова обычно непосредственно предшествуют числу.
    ПРАВЫЙ контекст («руб.», «₽», «RUB») — широкое окно 60 символов, потому что
    между числом и словом «рублей» может стоять сумма прописью.
    """
    left = text[max(0, start - 25): start].lower()
    right = text[end: end + 60].lower()
    return any(w in left for w in _LEFT_CONTEXT_WORDS) or any(
        w in right for w in _RIGHT_CONTEXT_WORDS
    )


def _is_negative(text: str, start: int) -> bool:
    """True, если перед числом стоит знак минус (отрицательная сумма).

    Защита от бага: «Сумма: -1250000.00 руб.» не должна возвращать 1250000.0.
    Для казначейства отрицательная сумма — это возврат, не приход.
    """
    # Смотрим 1-2 символа перед числом
    prefix = text[max(0, start - 2): start]
    # Если есть минус, отделённый пробелом или нет
    return bool(re.search(r"(?:^|\s)-\s*$", prefix))


def _looks_like_inn(text: str, start: int, end: int, raw: str) -> bool:
    """True, если число похоже на ИНН (10-12 цифр) и рядом есть маркер «ИНН».

    Защита от бага: «ИНН 7701234567, сумма 1 250 000 руб.» не должна
    возвращать 7701234567.0 как сумму.
    """
    digits_only = re.sub(r"\D", "", raw)
    if len(digits_only) not in (10, 12):
        return False
    # Проверяем, есть ли маркер «ИНН» в пределах 20 символов перед числом
    left = text[max(0, start - 20): start].lower()
    return "инн" in left


# Маппинг: длина числа → список маркеров реквизитов, которые исключают его из суммы
_REQUISITE_MARKERS: Final[dict[int, tuple[str, ...]]] = {
    9: ("бик",),  # БИК банка — 9 цифр
    10: ("инн",),  # ИНН юрлица — 10 цифр
    12: ("инн",),  # ИНН ИП — 12 цифр
    20: ("р/с", "р \\с", "к/с", "к \\с", "счёт", "счет", "сч. №"),  # расчётный/корр. счёт
}


def _looks_like_requisite(text: str, start: int, end: int, raw: str) -> bool:
    """True, если число похоже на банковский/налоговый реквизит (ИНН/БИК/р/с/к/с).

    Защита от бага: «Сумма: 40702810500000012345 руб.» не должна возвращать
    4.07e+19 — это расчётный счёт (20 цифр), не сумма.

    Логика:
      - 20 цифр — всегда р/с или к/с, не сумма (реальные суммы не бывают 10 квандрллионов)
      - 9/10/12 цифр — ИНН или БИК, ТОЛЬКО если raw состоит только из цифр
        (без разделителей тысяч). «1 250 000,00» после удаления не-цифр = 9 цифр,
        но это сумма с разделителями, не БИК.
    """
    digits_only = re.sub(r"\D", "", raw)
    n = len(digits_only)
    # 20-значные числа — всегда реквизит (р/с или к/с), без проверки маркера
    if n >= 20:
        return True
    # Для 9/10/12 цифр — проверяем, что raw это слитное число (без разделителей)
    # БИК/ИНН пишутся слитно: «044525225», «7701234567».
    # Суммы пишутся с разделителями: «1 250 000,00», «1,250,000.00».
    if not raw.isdigit():
        return False
    markers = _REQUISITE_MARKERS.get(n)
    if not markers:
        return False
    left = text[max(0, start - 25): start].lower()
    return any(m in left for m in markers)


def parse_amount(text: str) -> float | None:
    """Извлекает сумму из текста.

    Возвращает float или None, если сумму найти не удалось.
    Числовое совпадение засчитывается только при наличии контекста
    (руб / ₽ / RUB / сумма / итого / стоимость) — это защищает
    от ложных срабатываний на ИНН, БИК, р/с и подобных числах.

    Сумма прописью («девятьсот тысяч рублей») засчитывается и без числового
    контекста — она сама по себе является надёжным сигналом.

    Поддержка сокращений: «1250 тыс. руб.» → 1 250 000, «1.5 млн руб.» → 1 500 000.

    Финальный выбор — максимум из всех валидных кандидатов
    (числовых и прописью), т.к. итоговая сумма документа обычно наибольшая.
    """
    if not text:
        return None

    # Расширяем сокращения «тыс./млн./млрд. руб.» в полные числа
    expanded = _expand_abbreviation(text)
    text_to_search = expanded if expanded else text

    candidates: list[float] = []

    # 1. Числовые кандидаты с контекстом
    for pattern in _NUMERIC_PATTERNS:
        for m in pattern.finditer(text_to_search):
            raw = m.group(0)
            value = _numeric_match_to_float(raw)
            if value is None or value <= 0:
                continue
            # Защита от отрицательных сумм: «Сумма: -1250000.00 руб.» → None
            if _is_negative(text_to_search, m.start()):
                continue
            # Защита от ИНН/БИК/р/с: реквизиты не должны возвращаться как сумма
            if _looks_like_requisite(text_to_search, m.start(), m.end(), raw):
                continue
            # ТРЕБУЕМ контекст — иначе это не сумма, а случайное число
            if not _has_context(text_to_search, m.start(), m.end()):
                continue
            candidates.append(value)

    # 2. Кандидаты прописью
    for sm in _SPELL_OUT_RE.finditer(text_to_search):
        value = _spellout_to_int(sm.group(0))
        if value is not None and value > 0:
            candidates.append(float(value))

    if not candidates:
        return None

    # Берём максимум — обычно это итоговая сумма документа
    return max(candidates)
